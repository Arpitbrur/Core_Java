package String_methods;

public class charAt_02 {

	public static void main(String[] args) {
		String s1 = "today is monday";
		System.out.println(s1.charAt(3));
		String s2 = "rose is flower";
		System.out.println(s2.charAt(0));
		String s3 = "lets play cricket";
		System.out.println(s3.charAt(5));
		String s4 = "ram is a boy";
		System.out.println(s4.charAt(4));
		String s5 = "today is sunday";
		System.out.println(s5.charAt(0));
	}
}
